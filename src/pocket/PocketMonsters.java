package pocket;

public class PocketMonsters {
	private String name;
	private int level;
	private int attack;
	private int def;
	private int speed;
	private int spea;
	private int sped;
	private String status;
	
	public PocketMonsters(String name, int level, int attack, int def, int speed, int spea, int sped, String status) {
		this.name = name;
		this.level = level;
		this.attack = attack;
		this.def = def;
		this.speed = speed;
		this.spea = spea;
		this.sped = sped;
		this.status = status;
	}
	
	
}
